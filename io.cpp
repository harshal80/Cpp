#include <iostream>
using namespace std;
int main()
{
    int a;
    int b;
    cout << "Enter the 1st number :";
    cin >> a;
    cout << "Enter the 2nd number :";
    cin >> b;
    cout << "the sum of 1st & 2nd number:" << a + b << endl;
    cout << "the sum of 1st & 2nd number:" << a - b << endl;
    cout << "the sum of 1st & 2nd number:" << a * b << endl;
    cout << "the sum of 1st & 2nd number:" << a / b << endl;
    cout << "the sum of 1st & 2nd number:" << a % b << endl;
    cout << "the sum of 1st & 2nd number:" << a++ << endl;
    cout << "the sum of 1st & 2nd number:" << a-- << endl;

    return 0;
}
